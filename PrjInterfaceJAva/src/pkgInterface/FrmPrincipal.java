    private void exibirConta(ContaCorrente cc)
    {
        if (cc != null){
            jTextFieldAgencia.setText(Integer.toString(cc.getAgencia()));
            jTextFieldNumero.setText(Integer.toString(cc.getNumero()));
            jTextFieldSaque.setText(null);
            jTextFieldDeposito.setText(null);
            atualizarSaldo(cc.getSaldo());
        }
    }
