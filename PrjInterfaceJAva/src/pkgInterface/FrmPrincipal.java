    private void jButtonCriarContaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCriarContaActionPerformed
        // TODO add your handling code here:
        int numero, agencia;
        numero = Integer.parseInt(jTextFieldNumero.getText());
        agencia = Integer.parseInt(jTextFieldAgencia.getText());
        cc = new ContaCorrente(numero, agencia);
        String msg = "Conta criada!! " +
                "\nConta: " + cc.getNumero() +
                "\nAgência: " + cc.getAgencia();
                
        JOptionPane.showMessageDialog(this, msg);
    }//GEN-LAST:event_jButtonCriarContaActionPerformed

    private void jButtonDepositoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDepositoActionPerformed
        // TODO add your handling code here:
        double valor;
        valor = Double.parseDouble(jTextFieldDeposito.getText());
        cc.depositar(valor);
        this.atualizarSaldo(cc.getSaldo());
    }//GEN-LAST:event_jButtonDepositoActionPerformed

    private void atualizarSaldo(double saldo)
    {
        String sSaldo;
        sSaldo = String.format("R$ %.2f", saldo);
        jLabelSaldo.setText(sSaldo);
        if (saldo >= 0)
            jLabelSaldo.setForeground(Color.BLUE);
        else
            jLabelSaldo.setForeground(Color.RED);
    }
    
