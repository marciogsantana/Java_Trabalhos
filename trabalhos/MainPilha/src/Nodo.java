
public class Nodo {

    private int info;
	private Nodo prox;
	public Nodo(){
		this.prox = null;
		this.info = 0;   
	}
	public int getInfo() {
		return this.info;
	}
	public void setInfo(int info) {
		this.info = info;
	}
	public Nodo getProx() {
		return this.prox;
	}
	public void setProx(Nodo prox) {
		this.prox = prox;
	}
}

    
    
    
    
    
    
    
    
    
    
    
    

