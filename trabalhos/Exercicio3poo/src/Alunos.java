
public class Alunos {
    
  private String nome,cargo,endereco;
  
  public int idade,salario;
  
  
  public String nome(String nomes){ 

       nome = nomes;

       return(nome); 
       
  }

  public String cargos(String cargos){ 

       cargo = cargos;

       return(cargo); 
       
  }


  public String endereco(String enderecos){ 

       endereco = enderecos;

       return(enderecos); 
       
  }

 
  public int idade (int anos){

        idade = anos;
        
        return(idade);
  
  
  }
  
  
  public int salario (int valor){
  
       salario = valor;
       
       return(valor);
      
  }    
      
      
      
      
}
